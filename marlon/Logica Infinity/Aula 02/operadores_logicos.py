# Operadores Lógicos.


# AND - as duas condições precisam ser verdadeiras para que a expressão seja verdadeira.
print(1 == 1 and 2 == 0)

# OR - apenas uma condição precisa ser verdaira para que a expressão seja verdadeira.
print(1 > 2 or 10 < 14)

# NOT - inverter o resultado da condição, verdadeiro vira falso e falso vira verdadeiro.
print(not 1 > 5)